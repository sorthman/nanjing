package org.linlinjava.litemall.db.vo;

public class SyncOutsideVo {

	private String name;
	private String idCard;
	private String phone;
	private String community;
	private String address;
	private String arrivalDate;
	private String fromRegion;
	private String trackingRemark;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdCard() {
		return idCard;
	}

	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCommunity() {
		return community;
	}

	public void setCommunity(String community) {
		this.community = community;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getArrivalDate() {
		return arrivalDate;
	}

	public void setArrivalDate(String arrivalDate) {
		this.arrivalDate = arrivalDate;
	}

	public String getFromRegion() {
		return fromRegion;
	}

	public void setFromRegion(String fromRegion) {
		this.fromRegion = fromRegion;
	}

	public String getTrackingRemark() {
		return trackingRemark;
	}

	public void setTrackingRemark(String trackingRemark) {
		this.trackingRemark = trackingRemark;
	}

}
